function initials(name) {
  const clean = String(name || '').replace(/\[.*?\]/g, '').trim();
  if (!clean) return '“';
  return clean
    .split(/\s+/)
    .slice(0, 2)
    .map((w) => w[0])
    .join('')
    .toUpperCase();
}

