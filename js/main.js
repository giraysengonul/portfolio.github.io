/* =============================================
   CUSTOM CURSOR
   ============================================= */

function initCursor() {
  const cursor = document.getElementById('cursor');
  const ring = document.getElementById('cursor-ring');
  if (!cursor || !ring) return;

  // Only take over the cursor on real pointer devices; otherwise leave
  // the native cursor (keeps the custom cursor off touch/coarse devices).
  if (!window.matchMedia('(hover: hover) and (pointer: fine)').matches) return;
  document.body.classList.add('custom-cursor');

  let mouseX = 0, mouseY = 0;
  let ringX = 0, ringY = 0;

  document.addEventListener('mousemove', (e) => {
    mouseX = e.clientX;
    mouseY = e.clientY;
    cursor.style.left = mouseX + 'px';
    cursor.style.top = mouseY + 'px';
  });

  // Ring follows with smooth lag
  function animateRing() {
    ringX += (mouseX - ringX) * 0.12;
    ringY += (mouseY - ringY) * 0.12;
    ring.style.left = ringX + 'px';
    ring.style.top = ringY + 'px';
    requestAnimationFrame(animateRing);
  }
  animateRing();

  // Hover effect on interactive elements
  const interactives = document.querySelectorAll(
    'a, button, .project-category-card, .contact-card, .cert-card, .about-card'
  );

  interactives.forEach((el) => {
    el.addEventListener('mouseenter', () => {
      cursor.classList.add('hover');
      ring.classList.add('hover');
    });
    el.addEventListener('mouseleave', () => {
      cursor.classList.remove('hover');
      ring.classList.remove('hover');
    });
  });
}

/* =============================================
   NAVBAR SCROLL EFFECT
   ============================================= */

function initNavbar() {
  const navbar = document.getElementById('navbar');
  if (!navbar) return;

  window.addEventListener('scroll', () => {
    if (window.scrollY > 60) {
      navbar.classList.add('scrolled');
    } else {
      navbar.classList.remove('scrolled');
    }
  });
}

/* =============================================
   MOBILE MENU
   ============================================= */

function initMobileMenu() {
  const hamburger = document.getElementById('hamburger');
  const mobileMenu = document.getElementById('mobileMenu');
  const mobileClose = document.getElementById('mobileClose');
  const mobileLinks = document.querySelectorAll('.mobile-links a');

  if (!hamburger || !mobileMenu) return;

  hamburger.addEventListener('click', () => mobileMenu.classList.add('open'));
  mobileClose && mobileClose.addEventListener('click', () => mobileMenu.classList.remove('open'));

  mobileLinks.forEach((link) =>
    link.addEventListener('click', () => mobileMenu.classList.remove('open'))
  );
}

/* =============================================
   BACK TO TOP
   ============================================= */

function initBackToTop() {
  const btn = document.getElementById('backToTop');
  if (!btn) return;

  window.addEventListener('scroll', () => {
    btn.classList.toggle('visible', window.scrollY > 400);
  });

  btn.addEventListener('click', () =>
    window.scrollTo({ top: 0, behavior: 'smooth' })
  );
}

/* =============================================
   SMOOTH NAV LINK SCROLL
   ============================================= */

function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
    anchor.addEventListener('click', (e) => {
      const target = document.querySelector(anchor.getAttribute('href'));
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth' });
      }
    });
  });
}

/* =============================================
   UPDATE PROJECT COUNTS FROM projects.js
   ============================================= */

function updateProjectCounts() {
  if (typeof projects === 'undefined') return;

  const categoryMap = {
    'Tableau': 'tableau',
    'Power BI': 'powerbi',
    'Pandas': 'pandas',
    'SQL': 'sql',
    'Excel': 'excel',
    'Python': 'python'
  };

  document.querySelectorAll('.project-category-card').forEach(card => {
    const h3 = card.querySelector('h3');
    if (!h3) return;

    const categoryName = h3.textContent.trim();
    const categoryKey = categoryMap[categoryName];
    if (!categoryKey) return;

    const p = card.querySelector('p');
    if (!p) return;

    // Skip cards that don't show project counts (iOS Apps, iOS Training)
    if (!p.textContent.includes('Projects')) return;

    // Count projects in this category
    const count = projects.filter(proj => proj.category === categoryKey).length;

    // Update the count text
    p.textContent = `${count} ${count === 1 ? 'Project' : 'Projects'}`;
  });
}

/* =============================================
   SYNC HERO STAT COUNTS WITH REAL DATA
   Keeps the hero numbers + section subtitles in
   sync with projects.js / portfolioCerts so they
   never drift out of date.
   ============================================= */

function syncDynamicCounts() {
  const projectCount =
    typeof projects !== 'undefined' ? projects.length : null;
  const certCount = portfolioCerts.length;

  // Hero stat counters (read by animations.js via data-target)
  document.querySelectorAll('.stat-item').forEach((item) => {
    const label = item.querySelector('.stat-label');
    const number = item.querySelector('.stat-number[data-target]');
    if (!label || !number) return;

    const text = label.textContent.trim().toLowerCase();
    if (text.startsWith('project') && projectCount !== null) {
      number.dataset.target = projectCount;
    } else if (text.startsWith('certificate')) {
      number.dataset.target = certCount;
    }
  });

  // Section subtitles
  const projectsText = document.getElementById('projectsCountText');
  if (projectsText && projectCount !== null) projectsText.textContent = projectCount;

  const certsText = document.getElementById('certsCountText');
  if (certsText) certsText.textContent = certCount;
}

